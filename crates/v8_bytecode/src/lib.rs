//! v8_bytecode: Bytecode generation for the v8-rust JavaScript engine

pub mod generator;
pub mod instructions;

pub fn add(left: u64, right: u64) -> u64 {
    left + right
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        let result = add(2, 2);
        assert_eq!(result, 4);
    }
}
